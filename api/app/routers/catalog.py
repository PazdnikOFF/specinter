# Перенос legacy-функционала: второй тип связи товар↔товар + подписки на поступление.
# Схема db/schema.sql применяется только при init БД, поэтому для уже поднятого
# прода миграция догоняется на старте API (как в prices/quotes/settings).
# Критическая часть: без relation_type запросы карточки не выполнятся, поэтому она
# идёт отдельным вызовом — чтобы сбой необязательной части её НЕ откатил.
RELATIONS_MIGRATION_CORE = """
-- Тип связи: analog (legacy it_b_aarts) | install_option (legacy it_b_aarts2).
-- Дефолт 'analog' — существующие строки и блок «Аналоги» ведут себя ровно как раньше.
ALTER TABLE analogs ADD COLUMN IF NOT EXISTS relation_type text NOT NULL DEFAULT 'analog';

CREATE TABLE IF NOT EXISTS stock_alerts (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id  bigint REFERENCES products(id) ON DELETE CASCADE,
  email       text NOT NULL,
  name        text,
  phone       text,
  notified_at timestamptz,
  created_at  timestamptz DEFAULT now()
);
CREATE INDEX IF NOT EXISTS ix_stock_alerts_pending
  ON stock_alerts(product_id) WHERE notified_at IS NULL;
CREATE UNIQUE INDEX IF NOT EXISTS ux_stock_alerts_active
  ON stock_alerts(product_id, lower(email)) WHERE notified_at IS NULL;
"""

# Необязательная часть: ускорение и защита от дублей. Если тут что-то не сложится
# (напр. дедуп на большой таблице), карточка всё равно работает.
RELATIONS_MIGRATION_OPT = """
-- До этой миграции уникальности на analogs не было, и повторный прогон ETL мог
-- насажать дублей — снимаем их, иначе CREATE UNIQUE INDEX упадёт.
DELETE FROM analogs a USING analogs b
 WHERE a.id > b.id
   AND a.product_id = b.product_id
   AND a.relation_type = b.relation_type
   AND a.normalized_article IS NOT NULL
   AND a.normalized_article = b.normalized_article;

CREATE UNIQUE INDEX IF NOT EXISTS ux_analogs_uniq
  ON analogs(product_id, normalized_article, relation_type)
  WHERE normalized_article IS NOT NULL;
CREATE INDEX IF NOT EXISTS ix_analogs_rel ON analogs(product_id, relation_type);
"""


async def ensure_relations():
    async with db.pool.connection() as conn:
        await conn.execute(RELATIONS_MIGRATION_CORE)
    try:
        async with db.pool.connection() as conn:
            await conn.execute(RELATIONS_MIGRATION_OPT)
    except Exception as e:
        # Не валим старт: без уникального индекса ETL просто может насажать дублей,
        # но витрина и подписки работают.
        print("analogs dedup/index warning:", e)

