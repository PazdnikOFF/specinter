@router.post("/products/{product_id}/stock-alert")
async def create_stock_alert(product_id: int, body: dict = Body(...)):
    """Подписка «сообщить о поступлении» (перенос legacy it_b_notify).

    Письмо отправляет воркер, когда по товару появится offer с in_stock.
    Повторная подписка тем же email просто обновляет контакты — дубля не будет.
    """
    email = (body.get("email") or "").strip()
    if len(email) < 5 or "@" not in email or "." not in email.split("@")[-1]:
        raise HTTPException(422, "укажите корректный email")
    prod = await db.fetchone("SELECT id FROM products WHERE id=%s AND visible", (product_id,))
    if not prod:
        raise HTTPException(404, "product not found")
    await db.fetchone(
        """INSERT INTO stock_alerts (product_id, email, name, phone)
           VALUES (%s, %s, %s, %s)
           ON CONFLICT (product_id, lower(email)) WHERE notified_at IS NULL
           DO UPDATE SET name = EXCLUDED.name, phone = EXCLUDED.phone
           RETURNING id""",
        (product_id, email, (body.get("name") or "").strip() or None,
         (body.get("phone") or "").strip() or None))
    return {"ok": True}


